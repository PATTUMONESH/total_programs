package arrayprograming;

public class ArrayP2 {


    public static void main(String[] args){
        int a[]={3,2,5,4,6};



        //for loop
        for (int i=0;i< a.length;i++){
            System.out.println(a[i]);

        }

        System.out.println();

        //for each loop
        for (int h:a) {

            System.out.println(h);

        }




    }
}
